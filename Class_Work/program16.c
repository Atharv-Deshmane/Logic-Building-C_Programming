int main()
{
    int iValue = 0;
    bool bRet = false;

    printf("enter number\n");
    scanf("%d",&iValue);

    bRet = CheckEvenOdd(iValue);
    if (bRet == true)
    { printf("%d is Even number\n",iValue); }
    else
    {  printf("%d is Odd number\n",iValue); }
    
    return 0 ;
}


